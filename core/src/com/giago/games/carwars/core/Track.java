  private static int[] trackChanges = new int[] {0};
  
  private static final int TILE_SIZE_H = 20;
  private static final int TILE_SIZE_W = 300;

  private Image bgr;
  private Image bg;
  
  public Track(AssetManager assetManager) {
    loadImages(assetManager);
  }

  private void loadImages(AssetManager assetManager) {
    bg = assetManager.getImage("images/bg.png");
    bgr = assetManager.getImage("images/bgr.png");;
  }

  private int time = 0;
  private int delta = 0;
  private int center;
  private int speedSensation = -TILE_SIZE_H;
  
  public void paint(Surface surface, float alpha) {
    int h = surface.height();
    int w = surface.width();
    delta += trackChanges[time];
    center = h/2 + delta;
    int i = 0;
    while (i < w) {
      surface.drawImage(bg, i*TILE_SIZE_H + speedSensation, center-TILE_SIZE_W + (40-i)*trackChanges[time]);
      surface.drawImage(bgr, i*TILE_SIZE_H + speedSensation, center + (40-i)*trackChanges[time]);
      i++;
    }
    time++;
    if(time >= trackChanges.length) {
      time = 0;
    }
    if(speedSensation > 0) {
      speedSensation = -TILE_SIZE_H;
    } else {
      speedSensation += 3;
    }
  }

  public float getCenter() {
    return center;
  }
  
  

